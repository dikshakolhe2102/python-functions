import math

# 1. Basic Math
print("Basic Math:")
print("ceil(4.2):", math.ceil(4.2))
print("floor(4.7):", math.floor(4.7))
print("trunc(4.7):", math.trunc(4.7))
print("fabs(-5):", math.fabs(-5))
print("factorial(5):", math.factorial(5))
print("fmod(10, 3):", math.fmod(10, 3))
print()

# 2. Power and Logarithms
print("Power and Logarithms:")
print("pow(2, 3):", math.pow(2, 3))
print("sqrt(16):", math.sqrt(16))
print("exp(2):", math.exp(2))
print("log(8, 2):", math.log(8, 2))
print("log10(1000):", math.log10(1000))
print("log2(32):", math.log2(32))
print()

# 3. Trigonometry
print("Trigonometry:")
print("sin(pi / 2):", math.sin(math.pi / 2))
print("cos(0):", math.cos(0))
print("tan(pi / 4):", math.tan(math.pi / 4))
print("degrees(pi):", math.degrees(math.pi))
print("radians(180):", math.radians(180))
print()

# 4. Constants
print("Constants:")
print("pi:", math.pi)
print("e:", math.e)
print("infinite:", math.inf)
print("not a number:", math.nan) 
print()

# 5. Advanced Functions
print("Advanced Functions:")
print("gcd(48, 64):", math.gcd(48, 64))
print("isfinite(3.5):", math.isfinite(3.5))
print("isinf(inf):", math.isinf(math.inf))
print("isnan(nan):", math.isnan(math.nan))
